	function uf_print_cabecera($as_cedper,$as_nomper,$as_apeper,$as_descar,$ad_fecdesper,$ad_fechasper,$ai_i,&$io_cabecera,&$io_pdf)
	{
		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		//       Function: uf_print_cabecera
		//		   Access: private 
		//	    Arguments: as_cedper // Cédula del personal
		//	    		   as_nomper // Nombre del personal
		//	    		   as_apeper // Apellido del personal
		//	    		   as_descar // Decripción del cargo
		//	    		   ad_fecdesper // Fecha Desde
		//	    		   ad_fechasper // Fecha Hasta
		//	    		   ai_i // Consecutivo de recibos
		//	    		   io_cabecera // objeto cabecera
		//	    		   io_pdf // Objeto PDF
		//    Description: función que imprime la cabecera por personal
		//	   Creado Por: Ing. Miguel Palencia
		// Fecha Creación: 14/03/2007
		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		$io_pdf->saveState();
		$io_pdf->addText(20,385,11,$as_cedper); // Agregar la Cédula
		$io_pdf->addText(232,383,11,$as_nomper); // Agregar el Nombre
		$io_pdf->addText(400,383,11,$as_apeper); // Agregar el Apellido
		$io_pdf->addText(550,383,11,str_pad($ai_i,5,"0",0)); // Agregar el contador
		$io_pdf->addText(20,364,11,$as_descar); // Agregar el Cargo
		$io_pdf->addText(215,364,11,$ad_fecdesper); // Agregar la fecha desde
		$io_pdf->addText(298,364,11,$ad_fechasper); // Agregar la fecha hasta
		$io_pdf->addText(415,364,11,date("d/m/Y")); // Agregar la Fecha actual
		$io_pdf->restoreState();
		$io_pdf->closeObject();
		$io_pdf->addObject($io_cabecera,'all');
	}// end function uf_print_cabecera
